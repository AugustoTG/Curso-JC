const buscarTemperaturaCidade = async (nomeDaCidade) => {
    const dadosCidade = await fetch(
      `http://api.weatherapi.com/v1/forecast.json?q=${nomeDaCidade}&key=7fd89369164a44f29d4113450230402&days=5`,
      { mode: "cors" }
    );
    const cidadeInformacao = await dadosCidade.json();
    return cidadeInformacao;
  };
  
  const popularDOM = (infoCidade) => {
    const card = document.getElementById("cidade__card");
    card.innerHTML = "";
  
    const cidadeTitulo = document.createElement("p");
    cidadeTitulo.innerHTML = infoCidade.location.name;
    
    const estado = document.createElement("p");
    estado.innerHTML =infoCidade.location.region;
    
    const pais = document.createElement ("p");
    pais.innerHTML=infoCidade.location.country;
  
    const cidadeTemp = document.createElement("h3");
    cidadeTemp.innerHTML = infoCidade.current.temp_c;

    const cidadeCond = document.createElement("h3");
    cidadeCond.innerHTML = infoCidade.current.condition.text;

    const minimaCidade = document.createElement("h4");
    minimaCidade.innerHTML = infoCidade.forecast.forecastday[0].day.mintemp_c;

    const maximaCidade = document.createElement("h4");
    maximaCidade.innerHTML = infoCidade.forecast.forecastday[0].day.maxtemp_c;

    card.append(cidadeTitulo, estado, pais, cidadeTemp,cidadeCond, minimaCidade, maximaCidade);
  };
  
  const buscar = async () => {
    const nomeCidade = document.getElementById("nome__cidade");
    const infoCidade = await buscarTemperaturaCidade(nomeCidade.value);
    popularDOM(infoCidade);
  };


